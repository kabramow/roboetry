def wrapStringInHTMLWindows(program, poem_words, text):
    import datetime
    from webbrowser import open_new_tab

    now = datetime.datetime.today().strftime("%Y%m%d-%H%M%S")

    filename = program + '.html'
    f = open(filename,'w')

    wrapper = """<html>
    <head>
        <title>%s output - %s</title>
        <style>
            body {
                font-family: Georgia;
            }            
            #poem {
                background-color: white;
                color: black;
            }
            #blackout {
                background-color: black;
                color: navy;
            }
        </style>
    </head>
        <body>
            <p id=\"title\">
                Poem Generated By 
                <a href=\"https://github.com/kabramow/roboetry\">Roboetry</a> 
                from *****INSERT REF TO SOURCE MATERIAL
            </p>
            <div id=\"blackout\"><p>%s</p></div>
        </body>
    </html>"""

    whole = wrapper % (program, now, body_generator(poem_words, text))
    f.write(whole)
    f.close()

    open_new_tab(filename)


